#include "functions.h"
#include <cmath>

double sum(double A, double B) {
	return A + B;
}
double subtraction(double A, double B) {
	return A - B;
}
double multiplication(double A, double B) {
	return A * B;
}
double division(double A, double B) {
	return A / B;
}
double power(double A, double B) {
	return pow(A, B);
}

